				<p>Not only is the internet not going away, it is growing. According to research by <a href="http://www.pewresearch.org/about/" target="_blank">the Pew Research Center</a>, 87% of adults in the United States use the internet on a daily basis. With a number like that, I can't think of a reason why a business shouldn't have an online presence.</p>
				<h2>How ksolans.com Was Built</h2>
				<div class="jqm-block-content-clear">
					<p>ksolans.com was built using jQuery Mobile libraries without the use of a CMS. It is clean, lightweight, and completely responsive. Here was my process in building ksolans.com.</p>
					
					<p>I started by defining what pages were needed. The homepage is a given. But I decided to make that more of an "About Me" page instead of an overview of the site. Since Android and web development are different, I knew I needed a page for each. I considered adding social media in the web development page, but decided there would be enough content for it to have it's own page. I understand people do not want to spend ten minutes on a pages reading large blocks of text. Having a page for my resume was the main reason for creating the site, so it received it's own page. A contact page is necessary on just about every website, so for now, that would be my final page.</p>
