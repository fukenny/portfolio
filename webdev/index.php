				<div class="jqm-block-content-clear">
					<p>Content is key. This is what is going to drive people to your site and bring them back again and again. Setting up a system where adding additional content that is simple and user friendly is not the easiest of tasks. Luckily the development of Content Management Systems (CMS) helps take a lot of the pain out of the process.</p>
					<p>I've worked with a global company managing websites in over 16 countries using Joomla! and Liferay. Within that company I worked along side the editorial team to obtain the content to post and the design team to make sure each new page added fits with the company's brand standards. I also worked with the company's Director of Web Strategies to make updates to each of the sites within the firm around the globe. With this experience I learned how to communicate effectively across each team I needed to work with and how to multitask and prioritize assignments. This communication became especially important while I was working on websites where the content was not in english.</p>
