				<h2>Website Development</h2>
				<div class="ui-grid-a ui-responsive">
					<div class="ui-block-a">
						<div class="jqm-block-content-clear">
							<p>My approach to web development is simple. Find the message you want the user to get. That means forget all the bells and whistles and focus on your primary message and build off of that. That doesn't mean a site can't be unique, but I believe a clean sleek responsive design that only gives the information needed is best for the user. From there we can decide the best way to bring that message across.</p>
							<p>I also believe a website is never "finished". Going live is just a milestone. There is no point to a website that doesn't update itself with new content and new features. In certain cases it can even be dangerous and leave you vulnerable to being hacked.</p>
